package entity;

public class Introduction {
    private int id;

    private String location;

    private String phone;

    private String infocover;

    private String info;

    private String logo;

    private String cover;

    private String coverword;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getInfocover() {
        return infocover;
    }

    public void setInfocover(String infocover) {
        this.infocover = infocover;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getCover() {
        return cover;
    }

    public void setCover(String cover) {
        this.cover = cover;
    }

    public String getCoverword() {
        return coverword;
    }

    public void setCoverword(String coverword) {
        this.coverword = coverword;
    }
}